		<h2>Project Estimation</h2>
		<p>Early in a project the software development group and management must establish estimates for resources required, work to be done and time to delivery. Project planning is crucial to success. Technical people frequently do not take planning activities as seriously as they should and project cost; quality and time to delivery are often affected as a result.
		Cost estimating techniques are available based on metrics accumulated from past similar project experiences. The usual approach is to use several methods and compare values. If these values vary widely, then this variance is taken as an indication of the need for more information. COnstructive COst MOdel (COCOMO) II is a popular estimation model for conventional software. The parameters in this model have been derived from data from over 4,000 software projects. Estimation methods have also been developed for object-oriented and agile development. </p>
		<p>An important management observation is that software development time is not solely a function of the number of people on the project. One should not succumb quickly to the temptation to add more people to a late project. Adding more people could actually make it later.</p>
		
