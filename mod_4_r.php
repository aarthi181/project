		<h1>Software Project Management</h1>
		<p class="un">Standard processes and designs do not exist in the software field in the same way that they are found in handbooks of other engineering disciplines. People are the crucial element in project management and can be organised in teams that vary in their level of autonomy from traditional hierarchal structures to the 'self-organising' teams of the new agile paradigm.</p>
		<h2>People Capability Maturity Model (P-CMM)</h2>
		<p>This is a framework for continuously improving the management and development of the human assets of an organisation. It provides guidance in the following:
			<ul>
				<li>Characterising the maturity of workforce practices</li>
				<li>Setting priorities for immediate action</li>
				<li>Integrating workforce development with process improvement</li>
				<li>Becoming an employer of choice</li>
			</ul>
		</p>
		<p>It can be used alone or in conjunction with the SEI software process improvement programs.</p>
		
		<h2>Project Estimation</h2>
		<p class="un">Early in a project the software development group and management must establish estimates for resources required, work to be done and time to delivery. Project planning is crucial to success. Technical people frequently do not take planning activities as seriously as they should and project cost; quality and time to delivery are often affected as a result.
		Cost estimating techniques are available based on metrics accumulated from past similar project experiences. The usual approach is to use several methods and compare values. If these values vary widely, then this variance is taken as an indication of the need for more information. COnstructive COst MOdel (COCOMO) II is a popular estimation model for conventional software. The parameters in this model have been derived from data from over 4,000 software projects. Estimation methods have also been developed for object-oriented and agile development. </p>
		<p class="un">An important management observation is that software development time is not solely a function of the number of people on the project. One should not succumb quickly to the temptation to add more people to a late project. Adding more people could actually make it later.</p>
		
		<h2>Risk Management</h2>
		<p class="un">Anticipating and having a plan for potential project problems will help avoid “crisis management” when problems do occur.  The Spiral Process Model discussed earlier provides a framework for dealing with this issue. An investment of project management time in risk identification and monitoring can help keep potential problems to a minimum. Some examples of project risks are: changing requirements, low estimates of components reuse, high technical staff turnover and change in delivery deadline. </p>
